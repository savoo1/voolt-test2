    // Function to adjust the height of all slides to match the tallest one
    function setEqualSlideHeights() {
      const slides = document.querySelectorAll(".charged-swiper .swiper-wrapper .swiper-slide");
      let maxHeight = 0;

      // Reset heights to auto and find the maximum height
      slides.forEach((slide) => {
        slide.style.height = "auto"; // Reset height to auto for recalculation
        const slideHeight = slide.offsetHeight;
        if (slideHeight > maxHeight) {
          maxHeight = slideHeight;
        }
      });

      // Set all slides to the maximum height
      slides.forEach((slide) => {
        slide.style.height = `${maxHeight}px`;
      });
    }

    // Run the function on DOM load
    document.addEventListener("DOMContentLoaded", setEqualSlideHeights);

    // Run the function on window resize
    window.addEventListener("resize", setEqualSlideHeights);
